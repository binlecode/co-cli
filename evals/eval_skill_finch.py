"""Eval: skill-finch — skill dispatch + Finch character consistency.

Exercises the package-default `doctor` skill end-to-end:
  1. Skill body loading via the real _load_skills() parser
  2. Structural check: `check_capabilities` tool is invoked
  3. Character check: response is consistent with Finch (no emotional filler)
  4. Multi-turn: context carries through; follow-up is concrete and committed

Known issue targeted: doctor.md says "Keep it brief and friendly" — "friendly"
conflicts with Finch's unsentimental, load-bearing style. Case 2 is designed
to surface this conflict via a `forbidden` check and an LLM judge.

Prerequisites: LLM provider configured (ollama running, or gemini_api_key set).

Usage:
    uv run python evals/eval_skill_finch.py
"""

import asyncio
import time
from pathlib import Path
from typing import Any

from co_cli.commands._commands import _load_skills
from co_cli.agent import get_agent

from evals._common import (
    detect_model_tag,
    extract_tool_calls,
    make_eval_deps,
    make_eval_settings,
    score_turn,
)


# ---------------------------------------------------------------------------
# Skill loader
# ---------------------------------------------------------------------------


def _get_doctor_body() -> str:
    """Load the doctor skill body via the real _load_skills() parser.

    Exercises the actual frontmatter parsing path — if the skill file is
    broken the call raises here with a clear message rather than silently
    passing an empty body to the agent.

    Pass a non-existent skills_dir so only package-default skills are loaded.
    _load_skills() always loads package-default from its internal path first.
    """
    skills = _load_skills(Path(".co-cli/skills"))
    skill = skills.get("doctor")
    if skill is None:
        raise RuntimeError("doctor skill not found in skills/")
    return skill.body


# ---------------------------------------------------------------------------
# Single turn runner
# ---------------------------------------------------------------------------


async def _run_agent_turn(
    agent: Any,
    deps: Any,
    model_settings: Any,
    user_input: str,
    message_history: list,
    *,
    timeout_s: int = 120,
) -> tuple[str, list]:
    """Run one agent turn and return (response_text, all_messages).

    Raises asyncio.TimeoutError if the turn exceeds timeout_s.
    """
    eval_settings = make_eval_settings(model_settings)
    async with asyncio.timeout(timeout_s):
        result = await agent.run(
            user_input,
            deps=deps,
            model_settings=eval_settings,
            message_history=message_history,
        )
    text = str(result.output)
    messages = result.all_messages()
    return text, messages


# ---------------------------------------------------------------------------
# FIX report writer
# ---------------------------------------------------------------------------


def _write_fix_report(
    results: list[dict],
    model_tag: str,
    report_path: Path,
) -> None:
    """Write docs/FIX-skill-finch-eval.md with static + dynamic issues."""
    lines: list[str] = []

    lines.append("# FIX Report: Skill Dispatch + Finch Character")
    lines.append("")
    lines.append(f"Model: `{model_tag}`  ")
    from datetime import date
    lines.append(f"Date: {date.today().isoformat()}")
    lines.append("")
    lines.append(
        "Generated by `evals/eval_skill_finch.py`. "
        "Documents known gaps in the skill dispatch pipeline and Finch character adherence."
    )
    lines.append("")

    # --- Eval summary table ---
    lines.append("## Eval Summary")
    lines.append("")
    lines.append("| Case | Status | Failures |")
    lines.append("|------|--------|----------|")
    for r in results:
        status = "PASS" if r["status"] == "pass" else "FAIL"
        failures_str = "; ".join(r["failures"]) if r["failures"] else "—"
        lines.append(f"| `{r['id']}` | {status} | {failures_str} |")
    lines.append("")

    # --- Static issues (pre-populated regardless of eval outcome) ---
    lines.append("## Known Issues")
    lines.append("")
    lines.append("### FIX-01 — `friendly` instruction in doctor.md conflicts with Finch character")
    lines.append("")
    lines.append("**Severity:** moderate")
    lines.append("")
    lines.append("**Location:** `co_cli/skills/doctor.md`, last sentence of body")
    lines.append("")
    lines.append("**Root cause:**")
    lines.append(
        'The skill body ends with "Keep it brief and friendly." '
        '"Friendly" is an anti-pattern for Finch: load-bearing, unsentimental, no emotional filler. '
        "The skill body instruction can override the soul seed when the model "
        "interprets the prompt, causing warmth-substituting-for-substance responses."
    )
    lines.append("")
    lines.append("**Proposed fix:**")
    lines.append(
        'Replace `"Keep it brief and friendly."` with `"Be direct and specific."`'
    )
    lines.append("")
    lines.append(
        "**Test coverage:** case `doctor-finch-character`, `forbidden[\"friendly\"]` check"
    )
    lines.append("")

    # --- Dynamic issues from eval failures ---
    dynamic_failures = [r for r in results if r["failures"]]
    if dynamic_failures:
        lines.append("## Dynamic Issues (from eval run)")
        lines.append("")
        for r in dynamic_failures:
            lines.append(f"### Case `{r['id']}` failures")
            lines.append("")
            for f in r["failures"]:
                lines.append(f"- {f}")
            if r.get("response_excerpt"):
                lines.append("")
                lines.append("**Response excerpt:**")
                lines.append("```")
                lines.append(r["response_excerpt"][:400])
                lines.append("```")
            lines.append("")

    report_path.parent.mkdir(parents=True, exist_ok=True)
    report_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"FIX report written to {report_path}")


# ---------------------------------------------------------------------------
# Eval cases
# ---------------------------------------------------------------------------


async def run_eval() -> int:
    """Run all three eval cases. Returns exit code (0 = all pass, 1 = any fail)."""
    model_tag = detect_model_tag()
    print(f"eval_skill_finch  model={model_tag}")
    print()

    # Load the doctor skill body via the real parser
    doctor_body = _get_doctor_body()
    print(f"doctor skill body loaded ({len(doctor_body)} chars)")
    print()

    # Create a Finch-personality agent (soul seed baked into static system prompt)
    agent, model_settings, tool_names, _ = get_agent(personality="finch")
    print(f"Agent(finch) created with {len(tool_names)} tools")
    print()

    deps = make_eval_deps(
        session_id="eval-skill-finch",
        personality="finch",
    )

    results: list[dict] = []
    all_passed = True

    # -----------------------------------------------------------------------
    # Case 1 — doctor-tool-call (structural)
    # -----------------------------------------------------------------------
    print("Case 1/3: doctor-tool-call (structural) ...", end=" ", flush=True)
    case1: dict[str, Any] = {
        "id": "doctor-tool-call",
        "status": "pass",
        "failures": [],
        "response_excerpt": "",
    }
    t0 = time.monotonic()
    try:
        text1, messages1 = await _run_agent_turn(
            agent, deps, model_settings, doctor_body, []
        )
        elapsed1 = time.monotonic() - t0

        # Structural check: check_capabilities must be in the tool call list
        tool_calls = extract_tool_calls(messages1)
        tool_names_called = [tc[0] for tc in tool_calls]
        if "check_capabilities" not in tool_names_called:
            case1["status"] = "fail"
            case1["failures"].append(
                f"check_capabilities not called; tools called: {tool_names_called or '(none)'}"
            )
            all_passed = False

        case1["response_excerpt"] = text1[:300]
        status_str = "PASS" if case1["status"] == "pass" else "FAIL"
        print(f"{status_str} ({elapsed1:.1f}s)")
        if case1["failures"]:
            for f in case1["failures"]:
                print(f"  FAIL: {f}")

    except asyncio.TimeoutError:
        case1["status"] = "fail"
        case1["failures"].append("timeout: exceeded 120s")
        all_passed = False
        print("TIMEOUT")
    except Exception as exc:
        case1["status"] = "fail"
        case1["failures"].append(f"error: {exc}")
        all_passed = False
        print(f"ERROR: {exc}")

    results.append(case1)

    # -----------------------------------------------------------------------
    # Case 2 — doctor-finch-character (synchronous + LLM judge)
    # -----------------------------------------------------------------------
    print("Case 2/3: doctor-finch-character (character) ...", end=" ", flush=True)
    case2: dict[str, Any] = {
        "id": "doctor-finch-character",
        "status": "pass",
        "failures": [],
        "response_excerpt": "",
    }
    t0 = time.monotonic()
    try:
        text2, _ = await _run_agent_turn(
            agent, deps, model_settings, doctor_body, []
        )
        elapsed2 = time.monotonic() - t0

        checks2 = [
            {
                "type": "forbidden",
                "phrases": ["friendly", "happy to", "feel free", "great news"],
            },
            {
                "type": "llm_judge",
                "criteria": (
                    "Response is consistent with Finch character — direct, factual, "
                    "load-bearing sentences. No emotional filler, no reassurance, "
                    "no cheerfulness."
                ),
            },
        ]
        failures2, judge_details2 = await score_turn(
            text2, checks2, agent, deps, make_eval_settings(model_settings)
        )

        case2["response_excerpt"] = text2[:300]
        if failures2:
            case2["status"] = "fail"
            case2["failures"].extend(failures2)
            all_passed = False

        status_str = "PASS" if case2["status"] == "pass" else "FAIL"
        print(f"{status_str} ({elapsed2:.1f}s)")
        if case2["failures"]:
            for f in case2["failures"]:
                print(f"  FAIL: {f}")
        # Print judge reasoning for visibility regardless of outcome
        for idx, reasoning in judge_details2.items():
            print(f"  judge[{idx}]: {reasoning}")

    except asyncio.TimeoutError:
        case2["status"] = "fail"
        case2["failures"].append("timeout: exceeded 120s")
        all_passed = False
        print("TIMEOUT")
    except Exception as exc:
        case2["status"] = "fail"
        case2["failures"].append(f"error: {exc}")
        all_passed = False
        print(f"ERROR: {exc}")

    results.append(case2)

    # -----------------------------------------------------------------------
    # Case 3 — doctor-multi-turn (structural + commitment)
    # -----------------------------------------------------------------------
    print("Case 3/3: doctor-multi-turn (multi-turn) ...", end=" ", flush=True)
    case3: dict[str, Any] = {
        "id": "doctor-multi-turn",
        "status": "pass",
        "failures": [],
        "response_excerpt": "",
    }
    t0 = time.monotonic()
    try:
        # Turn 1: doctor skill body → check_capabilities called
        text3a, messages3 = await _run_agent_turn(
            agent, deps, model_settings, doctor_body, []
        )
        prior_len = len(messages3)

        tool_calls3 = extract_tool_calls(messages3)
        tool_names3 = [tc[0] for tc in tool_calls3]
        if "check_capabilities" not in tool_names3:
            case3["status"] = "fail"
            case3["failures"].append(
                f"turn1: check_capabilities not called; tools: {tool_names3 or '(none)'}"
            )
            all_passed = False

        # Turn 2: follow-up question — must get a concrete, committed recommendation
        text3b, messages3b = await _run_agent_turn(
            agent, deps, model_settings,
            "What should I set up first?",
            messages3,
        )
        elapsed3 = time.monotonic() - t0

        checks3b = [
            {
                "type": "llm_judge",
                "criteria": (
                    "Gives a concrete, prioritized recommendation without hedging, "
                    "asking clarifying questions, or saying 'it depends'. "
                    "Names a specific thing to set up first."
                ),
            },
        ]
        failures3b, judge_details3b = await score_turn(
            text3b, checks3b, agent, deps, make_eval_settings(model_settings)
        )

        case3["response_excerpt"] = text3b[:300]
        if failures3b:
            case3["status"] = "fail"
            case3["failures"].extend([f"turn2: {f}" for f in failures3b])
            all_passed = False

        status_str = "PASS" if case3["status"] == "pass" else "FAIL"
        print(f"{status_str} ({elapsed3:.1f}s)")
        if case3["failures"]:
            for f in case3["failures"]:
                print(f"  FAIL: {f}")
        for idx, reasoning in judge_details3b.items():
            print(f"  judge[{idx}]: {reasoning}")

    except asyncio.TimeoutError:
        case3["status"] = "fail"
        case3["failures"].append("timeout: exceeded 120s")
        all_passed = False
        print("TIMEOUT")
    except Exception as exc:
        case3["status"] = "fail"
        case3["failures"].append(f"error: {exc}")
        all_passed = False
        print(f"ERROR: {exc}")

    results.append(case3)

    # -----------------------------------------------------------------------
    # Summary
    # -----------------------------------------------------------------------
    print()
    passed_count = sum(1 for r in results if r["status"] == "pass")
    print(f"Results: {passed_count}/{len(results)} passed")
    print()

    # Write FIX report regardless of pass/fail outcome
    report_path = Path(__file__).parent.parent / "docs" / "FIX-skill-finch-eval.md"
    _write_fix_report(results, model_tag, report_path)

    return 0 if all_passed else 1


# ---------------------------------------------------------------------------
# Entrypoint
# ---------------------------------------------------------------------------


if __name__ == "__main__":
    import sys
    exit_code = asyncio.run(run_eval())
    sys.exit(exit_code)
